#include "libft.h"

int	ft_isnum(char *str)
{
	int	i;

	i = 0;
	if (str[i] == '\0' || str[i] == '-' || str[i] == '\n')
		return (0);
	while (ft_isdigit(str[i++]))
		;
	if (str[i] != '\n')
		return(0);

}
